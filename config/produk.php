                <p>1. Pertamax Racing</p>
                <p>Merupakan bahan bakar kendaraan yang diakui federasi balap internasional, menjadikan mesin lebih responsive, lebih stabil, dan memiliki daya tahan yang tinggi, serta bersahabat dengan lingkungan. Pertamax Racing memiliki oktan minimal 100 yang khusus diperuntukkan bagi kendaraan balap dan kendaraan yang memiliki kompresi mesin lebih tinggi dari 13:1</p>
                <p>2. Pertamax Turbo</p>
                <p>Merupakan bahan bakar untuk kendaraan bermesin bensin yang dikembangkan bersama antara Pertamina dan Lamborghini yang dirancang untuk memenuhi persyaratan mesin berteknologi tinggi. Pertamax Turbo pertama kali diluncurkan di Belgia sebagai bahan bakar resmi pada Lamborghini Supertrofeo European Series pada 29 Juli 2016</p>
                <p>3. Pertamax</p>
                <p>Merupakan bahan bakar bensin dengan angka oktan minimal 92 berstandar international. Pertamax sangat direkomendasikan untuk digunakan pada kendaraan yang memiliki kompresi rasio 10:1 hingga 11:1 atau kendaraan berbahan bakar bensin yang menggunakan teknologi setara dengan Electronic Fuel Injection (EFI).
                <p>Dengan ecosave technology, Pertamax mampu membersihkan bagian dalam mesin (detergency), Pertamax juga dilengkapi dengan pelindung anti karat pada dinding tangki kendaraan, saluran bahan bakar dan ruang bakar mesin (corrotion inhibitor), serta mampu menjaga kemurnian bahan bakar dari campuran air sehingga pembakaran menjadi lebih sempurna (demulsifier).</p>
                <p>4. Pertalite</p>
                <p>Merupakan bahan bakar gasoline yang memiliki angka oktan 90 serta berwarna hijau terang dan jernih ini sangat tepat digunakan oleh kendaraan dengan kompresi 9:1 hingga 10:1. Bahan bakar Pertalite memiliki angka oktan yang lebih tinggi daripada bahan bakar Premium 88 sehingga lebih tepat digunakan untuk kendaraan bermesin bensin yang saat ini beredar di Indonesia. Dengan tambahan additive, Pertalite mampu menempuh jarak yang lebih jauh dengan tetap memastikan kualitas dan harga yang terjangkau.</p>
                <p>5. Premium</p>
                <p>Merupakan bahan bakar mesin bensin dengan angka oktan minimal 88 diproduksi sesuai dengan Keputusan Direktorat Jenderal Minyak dan Gas Bumi Np.3674/K24/DJM/2006 tanggal 17 Maret 2006 tentang Spesifikasi Bahan Bakar Minyak Jenis Bensin 88. Premium dapat digunakan pada kendaraan bermotor bensin dengan risiko kompresi rendah (dibawah 9:1).</p>
